def main() -> None:
    agent = AgentConfig(
        agent_id="agent-credit-001",
        name="Credit Decision Agent",
        allowed_tools=["read_customer_data", "credit_score_api"],
        write_permissions=False,
        autonomy_level="LOW",
        max_transaction_amount=0.0,
        owner="Head of Credit AI"
    )

    engine = GovernanceEngine(agent)

    print("\n=== RUNNING CREDIT DECISION WORKFLOW ===")
    result = engine.run_credit_decision_workflow(customer_id="CUST-1001")

    print("\n=== FINAL RESULT ===")
    for key, value in result.items():
        print(f"{key}: {value}")

    print("\n=== CONTINUOUS RUNTIME GOVERNANCE LOOP ===")
    engine.continuous_runtime_loop(cycles=3)


if __name__ == "__main__":
    main()

